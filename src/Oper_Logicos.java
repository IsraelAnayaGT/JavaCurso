public class Oper_Logicos {
    static void main() {
        boolean a = true;
        boolean b = false;

        //Operador AND (Y)
        System.out.println("a && b: " + (a && b)); // AND lógico
        //Operador OR (O)
        System.out.println("a || b: " + (a || b)); // OR lógico
        //Operador NOT (NO)
        System.out.println("!a: " + (!a)); // NOT lógico

        //Operador XOR (O exclusivo)
        System.out.println("a ^ b: " + (a ^ b)); // XOR lógico
    }
}
